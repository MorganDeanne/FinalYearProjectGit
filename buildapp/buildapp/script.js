function piecesTemplate(item) {
//    For each thing, make an id that is unique
    document.getElementById("results").innerHTML = ` `;
    return `
      <div id="${item.Id}" class="individual_piece" onclick="individualView(this.id);">
        <img class="piece_photo" src="${item.Photo}"/>
        <h2 class="piece_name">${item.Name}</h2>
        <p class="piece_date">${item.Date}</p>
      </div>
    `
    
};

function individualView(id){
    document.getElementById("results").innerHTML = ` 
    <div class="detailView">
        <img id="detailPhoto" src="${ListOfObjects[id-1].Photo}"/>
        <h3 id="detailName">${ListOfObjects[id-1].Name}</h3>
        <h3 id="detailDate">${ListOfObjects[id-1].Date}</h3>
        <h3 class="detail">Museum Number</h3>
        <p  class="detail">${ListOfObjects[id-1]["Museum Number"]}</p>
        <h3 class="detail">Material</h3>
        <p  class="detail">${ListOfObjects[id-1].Material}</p>
        <h3 class="detail">Material</h3>
        <p  class="detail">${ListOfObjects[id-1].Provance}</p>
        <h3 class="detail">Mark Type</h3>
        <p  class="detail">${ListOfObjects[id-1]["Mark Type"]}</p>
        <h3 class="detail">Factory/Collection</h3>
        <p  class="detail">${ListOfObjects[id-1]["Factory/Collection"]}</p>
        <h3 class="detail">Artist</h3>
        <p  class="detail">${ListOfObjects[id-1].Artist}</p>
        <h3 class="detail">Description</h3>
        <p  class="detail">${ListOfObjects[id-1].Desc}</p>
        <div id="favButton"> 
            <button id="${ListOfObjects[id-1].Id}" class="favButton" onclick="addToFavourites(this.id)">Add to Favourites</button>
            <button id="${ListOfObjects[id-1].Id}" class="removeFavButton" onclick="removeFavourites(this.id)">Remove From Favourites</button>
        </div>
    </div>
    `
    ;
};

function addToFavourites(id) {
    if(ListOfObjects[id-1].favourited == 0){
        ListOfObjects[id-1].favourited = 1;
        favouriteList = ListOfObjects[id-1]
        console.log(favouriteList)
    }  
}

function removeFavourites(id) {
    if(ListOfObjects[id-1].favourited == 1){
        ListOfObjects[id-1].favourited = 0;
        favouriteList = ListOfObjects[id-1]
        console.log(favouriteList)
    }  
}


